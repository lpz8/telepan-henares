CREATE TABLE public.clientes (
  id            UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id       UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  codigo        TEXT,
  nombre        TEXT NOT NULL,
  direccion     TEXT,
  codigo_postal TEXT,
  poblacion     TEXT,
  provincia     TEXT DEFAULT 'GUADALAJARA',
  telefono1     TEXT,
  telefono2     TEXT,
  forma_pago    TEXT DEFAULT 'Efectivo',
  es_alterno    BOOLEAN DEFAULT FALSE,
  observaciones TEXT,
  orden_ruta    INTEGER DEFAULT 0,
  created_at    TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE public.productos (
  id             UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id        UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  nombre         TEXT NOT NULL,
  categoria      TEXT DEFAULT 'Pan',
  precio_sin_iva NUMERIC(10,2) DEFAULT 0,
  iva            NUMERIC(5,2)  DEFAULT 4,
  activo         BOOLEAN DEFAULT TRUE,
  created_at     TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE public.pedidos_modelo (
  id          UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id     UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  cliente_id  UUID REFERENCES public.clientes(id) ON DELETE CASCADE,
  producto_id UUID REFERENCES public.productos(id) ON DELETE CASCADE,
  dia_semana  INTEGER NOT NULL CHECK (dia_semana BETWEEN 0 AND 6),
  cantidad    NUMERIC(10,2) DEFAULT 1,
  frecuencia  TEXT DEFAULT 'todos',
  created_at  TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE public.pedidos (
  id          UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id     UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  cliente_id  UUID REFERENCES public.clientes(id)  ON DELETE SET NULL,
  producto_id UUID REFERENCES public.productos(id) ON DELETE SET NULL,
  fecha       DATE NOT NULL,
  cantidad    NUMERIC(10,2) DEFAULT 1,
  precio      NUMERIC(10,2) DEFAULT 0,
  iva         NUMERIC(5,2)  DEFAULT 4,
  created_at  TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE public.facturas (
  id          UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id     UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  numero      TEXT NOT NULL,
  cliente_id  UUID REFERENCES public.clientes(id) ON DELETE SET NULL,
  fecha       DATE,
  mes         TEXT,
  tipo_pago   TEXT DEFAULT 'Efectivo',
  base        NUMERIC(10,2) DEFAULT 0,
  iva_total   NUMERIC(10,2) DEFAULT 0,
  total       NUMERIC(10,2) DEFAULT 0,
  base4       NUMERIC(10,2) DEFAULT 0,
  cuota4      NUMERIC(10,2) DEFAULT 0,
  base10      NUMERIC(10,2) DEFAULT 0,
  cuota10     NUMERIC(10,2) DEFAULT 0,
  base21      NUMERIC(10,2) DEFAULT 0,
  cuota21     NUMERIC(10,2) DEFAULT 0,
  pagado      BOOLEAN DEFAULT FALSE,
  fecha_pago  DATE,
  created_at  TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE public.lineas_factura (
  id              UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  factura_id      UUID REFERENCES public.facturas(id) ON DELETE CASCADE,
  producto_nombre TEXT,
  cantidad        NUMERIC(10,2),
  precio          NUMERIC(10,2),
  iva             NUMERIC(5,2),
  created_at      TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE public.gastos (
  id         UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  user_id    UUID REFERENCES auth.users(id) ON DELETE CASCADE,
  concepto   TEXT NOT NULL,
  categoria  TEXT DEFAULT 'General',
  importe    NUMERIC(10,2) DEFAULT 0,
  fecha      DATE DEFAULT CURRENT_DATE,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

CREATE TABLE public.proveedores (
